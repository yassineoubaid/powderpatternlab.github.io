import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { JSDOM } from 'jsdom';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..');
const outputDir = path.join(rootDir, 'src', 'data', 'generated');
const outputFile = path.join(outputDir, 'scatteringData.ts');

const GEMMI_IT92_URL =
  'https://raw.githubusercontent.com/project-gemmi/gemmi/master/include/gemmi/it92.hpp';
const NIST_LENGTHS_URL =
  'https://www.nist.gov/ncnr/planning-your-experiment/scattering-length-periodic-table';

function cleanNumericToken(token) {
  return token
    .replaceAll('−', '-')
    .replaceAll('–', '-')
    .replace(/\([^)]*\)/g, '')
    .trim();
}

function parseRealNumber(value) {
  const cleaned = cleanNumericToken(value).replace(/\s+/g, ' ');
  const match = cleaned.match(/[-+]?\d*\.?\d+(?:e[-+]?\d+)?/i);
  if (!match) {
    return null;
  }

  return Number(match[0]);
}

function formatNumber(value) {
  if (Number.isInteger(value)) {
    return `${value}`;
  }

  return `${value}`;
}

async function fetchText(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }

  return response.text();
}

function parseGemmiFormFactors(source) {
  const ordinarySection = source.split('// Atoms with charge')[0];
  const lines = ordinarySection.split('\n');
  const factors = [];

  for (const line of lines) {
    const match = line.match(/\{([^}]+)\},\s*\/\/\s*([A-Za-z0-9=+-]+)/);
    if (!match) {
      continue;
    }

    const symbol = match[2].trim();
    if (symbol.startsWith('X=')) {
      continue;
    }

    const values = match[1].split(',').map((token) => Number(token.trim()));
    if (values.length !== 9 || values.some((value) => Number.isNaN(value))) {
      continue;
    }

    factors.push({
      symbol,
      z: factors.length + 1,
      a: values.slice(0, 4),
      b: values.slice(4, 8),
      c: values[8],
    });
  }

  return factors;
}

async function parseNistNeutronLengths() {
  const page = await fetchText('https://ncnr.nist.gov/resources/n-lengths/elements/h.html');
  const dom = new JSDOM(page);
  const neutronLengths = {};

  for (const row of [...dom.window.document.querySelectorAll('tr.element-row')]) {
    const id = row.getAttribute('id')?.trim();
    if (!id) {
      continue;
    }

    const cells = [...row.querySelectorAll('td')].map((cell) =>
      cell.textContent?.trim() ?? '',
    );

    if (cells.length < 6) {
      continue;
    }

    const symbol = cells[0];
    const bc = parseRealNumber(cells[5]);

    if (!symbol || bc === null || Number.isNaN(bc)) {
      continue;
    }

    neutronLengths[symbol] = bc;
  }

  return neutronLengths;
}

function renderObjectEntries(entries, formatter) {
  return entries
    .map(([key, value]) => `  ${JSON.stringify(key)}: ${formatter(value)}`)
    .join(',\n');
}

async function main() {
  const [gemmiSource, neutronLengths] = await Promise.all([
    fetchText(GEMMI_IT92_URL),
    parseNistNeutronLengths(),
  ]);

  const factors = parseGemmiFormFactors(gemmiSource);
  const elements = factors.map(({ symbol, z }) => ({
    symbol,
    z,
    hasNeutron: Object.hasOwn(neutronLengths, symbol),
  }));

  const xrayEntries = factors.map(({ symbol, z, a, b, c }) => [
    symbol,
    `{ z: ${z}, a: [${a.map(formatNumber).join(', ')}], b: [${b
      .map(formatNumber)
      .join(', ')}], c: ${formatNumber(c)} }`,
  ]);
  const neutronEntries = Object.entries(neutronLengths).sort(([left], [right]) =>
    left.localeCompare(right),
  );
  const elementEntries = elements
    .map(
      ({ symbol, z, hasNeutron }) =>
        `  { symbol: ${JSON.stringify(symbol)}, z: ${z}, hasNeutron: ${hasNeutron} }`,
    )
    .join(',\n');

  const fileContents = `/* eslint-disable */
// Auto-generated by scripts/generate-scattering-data.mjs
// Sources: Gemmi IT92 coefficients and NIST NCNR neutron scattering lengths.

export type ElementRecord = {
  symbol: string;
  z: number;
  hasNeutron: boolean;
};

export type XrayFormFactorRecord = {
  z: number;
  a: [number, number, number, number];
  b: [number, number, number, number];
  c: number;
};

export const ELEMENTS: ElementRecord[] = [
${elementEntries}
];

export const XRAY_FORM_FACTORS: Record<string, XrayFormFactorRecord> = {
${renderObjectEntries(xrayEntries, (value) => value)}
};

export const NEUTRON_SCATTERING_LENGTHS: Record<string, number> = {
${renderObjectEntries(neutronEntries, (value) => formatNumber(value))}
};
`;

  await mkdir(outputDir, { recursive: true });
  await writeFile(outputFile, fileContents, 'utf8');
  console.log(`Generated ${path.relative(rootDir, outputFile)}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
